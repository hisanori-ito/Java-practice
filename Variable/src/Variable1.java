public class Variable {
	public static void main(String[] args) {
		// int型の変数を宣言
		int num1;
		
		// String型の変数を宣言
		String str1;
		
		//それぞれ初期化
		num1 = 10;
		str1 = "Hello World!";
		
		//出力
		System.out.println(num1);
		System.out.println(str1);
		
		//num1に再代入して出力
		num1 = 20;
		System.out.println(num1);
		
		//int型の変数num2を宣言し出力
		int num2 = 100;
		System.out.println(num2);
	}
}
